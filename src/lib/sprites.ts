const HAT_LINES: Record<Hat, string> = {
  none: "",
  crown: "   \\^^^/    ",
  tophat: "   [___]    ",
  propeller: "    -+-     ",
  halo: "   (   )    ",
  wizard: "    /^\\     ",
  beanie: "   (___)    ",
  tinyduck: "    ,>      ",
};

const IDLE_SEQUENCE = [0, 0, 0, 0, 1, 0, 0, 0, -1, 0, 0, 2, 0, 0, 0] as const;

export function renderSprite(
  species: Species,
  eye: Eye | "-",
  hat: Hat,
  frame: number,
  sleeping: boolean,
): string[] {
  const frames = BODIES[species];
  const eyeChar = sleeping ? "-" : eye;
  const body = frames[frame % frames.length].map((line) =>
    line.replaceAll("{E}", eyeChar),
  );
  const lines = [...body];
  if (hat !== "none" && !lines[0].trim()) {
    lines[0] = HAT_LINES[hat];
  }
  if (!lines[0].trim() && frames.every((f) => !f[0].trim())) {
    lines.shift();
  }
  return lines;
}

export function renderAnimatedSprite(
  species: Species,
  eye: Eye,
  hat: Hat,
  frame: number,
): string[] {
  const step = IDLE_SEQUENCE[frame % IDLE_SEQUENCE.length];
  const sleeping = step === -1;
  const spriteFrame = sleeping ? 0 : step;
  return renderSprite(species, eye, hat, spriteFrame, sleeping);
}

export const IDLE_SEQUENCE_LENGTH = IDLE_SEQUENCE.length;
